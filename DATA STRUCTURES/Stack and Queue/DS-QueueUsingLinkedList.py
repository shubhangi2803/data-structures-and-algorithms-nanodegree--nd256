class Node:
    def __init__(self,value):
        self.value=value
        self.next=None

class Queue:
    def __init__(self):
        self.head=None
        self.tail=None
        self.num_elements=0

    def enqueue(self,value):
        if self.num_elements==0:
            self.head=Node(value)
            self.tail=self.head
        else:
            temp=Node(value)
            self.tail.next=temp
            temp=self.tail
        self.num_elements+=1

    def size(self):
        return self.num_elements
    
    def is_empty(self):
        return self.size()==0

    def dequeue(self):
        if self.is_empty():
            return True
        x=self.head.value
        self.head=self.head.next
        self.num_elements-=1
        return x

